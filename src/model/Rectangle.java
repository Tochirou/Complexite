package model;

public class Rectangle {
    
    private int largeur;
    private int hauteur;
    
    public Rectangle(int largeur, int hauteur) {
        this.largeur=largeur;
        this.hauteur=hauteur;
    }
    
    public int getHauteur() {
        return hauteur;
    }
    
    public int getLargeur() {
        return largeur;
    }
    

}
